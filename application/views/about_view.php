<h1>About</h1>

<p>
<b>Motriba.com</b> is a place where you can vote daily on any leader in the world from wherever you are. You can vote for the leader of the country you belong to, and any other country in the world just by clicking Like or Dislike. Also, you can see real-time votes coming from everywhere, allowing you to track the popularity of these leaders and how well they are doing
